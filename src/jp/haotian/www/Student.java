package jp.haotian.www;

/**
 * Student Class
 * @author deepin
 * @time 2019/04/19
 * 
 */
public class Student {
	//Student ID
	private int id;
	//Student name
	private String name;
	
	/**
	 * Get Student id
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * Set Student ID
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * Get Student Name
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	
	/**
	 * Set Student Name
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	
}
